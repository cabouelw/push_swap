#include "libft.h"

long int	ft_atoi(const char *s)
{
	int			sign;
	long int	n;

	n = 0;
	sign = 1;
	if (*s == '-')
	{
		sign = -1;
		s++;
	}
	else if (*s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		n = n * 10 + sign * (*s - '0');
		s++;
	}
	return (n);
}